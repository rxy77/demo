package com.rxy.demo.Demo11;
/*
使用接口时的注意事项：
1、接口没有静态代码块和构造方法
2、一个类的直接父类只有唯一一个，但一个类可以同时实现多个接口
格式：
public class 实现类名称 implements 接口名称，接口名称{
    //覆盖重写所有接口的抽象方法
}
3、如果实现类所实现的多个接口当中，有重复的抽象方法，那么只需要覆盖重写一次
4、如果实现类没有覆盖重写所有接口当中的抽象类，那么这个实现类必须是抽象类
5、如果实现类所实现的多个接口当中，有重复的默认方法，那么实现类(包括抽象类)一定要对重复的方法覆盖重写
6、一个类，如果父类的方法和其中一个接口的默认方法产生了冲突，优先使用父类方法
 */
public class Demo01Interface {
    public static void main(String[] args) {
        Zi zi=new Zi();
        zi.method();
    }
}
